package com.xy.cms.entity;

public class AppRole {
	/**
	 * 主键id
	 * */
	private Long id;
	
	/**
	 * 角色名称
	 * */
	private String name;
	

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	
	
}
