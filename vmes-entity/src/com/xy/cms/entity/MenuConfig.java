package com.xy.cms.entity;

public class MenuConfig {
	public Long id;
	public Long layerWidth;
	public Long layerHeight;
	public Long menuId;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getLayerWidth() {
		return layerWidth;
	}
	public void setLayerWidth(Long layerWidth) {
		this.layerWidth = layerWidth;
	}
	public Long getLayerHeight() {
		return layerHeight;
	}
	public void setLayerHeight(Long layerHeight) {
		this.layerHeight = layerHeight;
	}
	public Long getMenuId() {
		return menuId;
	}
	public void setMenuId(Long menuId) {
		this.menuId = menuId;
	}
	
	
	
}
